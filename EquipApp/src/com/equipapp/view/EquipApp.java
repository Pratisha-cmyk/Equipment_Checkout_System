    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
  
    int selectedRowIndex = tblProduct.getSelectedRow();
    if (selectedRowIndex == -1) {
        JOptionPane.showMessageDialog(this, "Please select a row to update.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    boolean isValid = true;
    StringBuilder errors = new StringBuilder();

    if (!txtFldEquipID.getText().isEmpty() && !ValidationUtil.isValidEquipmentId(txtFldEquipID.getText())) {
        validateField(txtFldEquipID, "Equipment ID", lblErrorMsgEquipID, "Must be a positive number.", errorColor, successColor, false);
        errors.append("Invalid Equipment ID.\n");
        isValid = false;
    }

    if (!txtFldEqipName.getText().isEmpty() && !ValidationUtil.isValidEquipmentName(txtFldEqipName.getText())) {
        validateField(txtFldEqipName, "Equipment Name", lblErrorMsgEquipmentName, "Only letters and spaces allowed.", errorColor, successColor, false);
        errors.append("Invalid Equipment Name.\n");
        isValid = false;
    }

    if (!txtFldStudentI.getText().isEmpty() && !ValidationUtil.isValidBorrowerStudentId(txtFldStudentI.getText())) {
        validateField(txtFldStudentI, "Student ID", lblErrorMsgStudentID, "Must be a positive number.", errorColor, successColor, false);
        errors.append("Invalid Student ID.\n");
        isValid = false;
    }

    if (!txtFldStudentName.getText().isEmpty() && !ValidationUtil.isValidBorrowerName(txtFldStudentName.getText())) {
        validateField(txtFldStudentName, "Student Name", lblErrorMsgStudentName, "Only letters and spaces allowed.", errorColor, successColor, false);
        errors.append("Invalid Student Name.\n");
        isValid = false;
    }

    if (!txtFldPhone.getText().isEmpty() && !ValidationUtil.isValidBorrowerPhone(txtFldPhone.getText())) {
        validateField(txtFldPhone, "Phone", lblErrorMsgPhoneNo, "Phone must be 7 to 15 digits (optional +).", errorColor, successColor, false);
        errors.append("Invalid Phone.\n");
        isValid = false;
    }

    if (!txtFldAddress.getText().isEmpty() && !ValidationUtil.isValidBorrowerDepartment(txtFldAddress.getText())) {
        validateField(txtFldAddress, "Department", lblErrorMsgAddress, "Only letters and spaces allowed.", errorColor, successColor, false);
        errors.append("Invalid Department.\n");
        isValid = false;
    }

    if (!txtFldCustID1.getText().isEmpty() && !ValidationUtil.isValidDate(txtFldCustID1.getText())) {
        validateField(txtFldCustID1, "Due Date", lblErrorMsgDueDate, "Use format YYYY-MM-DD.", errorColor, successColor, false);
        errors.append("Invalid Due Date.\n");
        isValid = false;
    }

    if (!isValid) {
        JOptionPane.showMessageDialog(this, errors.toString(), "Validation Errors", JOptionPane.ERROR_MESSAGE);
        return;
    }

    EquipmentModel selected = equipmentList.get(selectedRowIndex);

    if (!txtFldEquipID.getText().isEmpty()) {
        int newId = Integer.parseInt(txtFldEquipID.getText().trim());
        if (newId != selected.getEquipmentId()) {
            for (int i = 0; i < equipmentList.size(); i++) {
                if (i != selectedRowIndex && equipmentList.get(i).getEquipmentId() == newId) {
                    JOptionPane.showMessageDialog(this, "Equipment ID already exists.", "Duplicate Equipment ID", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        }
        selected.setEquipmentId(newId);
    }

    if (!txtFldEqipName.getText().isEmpty()) {
        selected.setEquipmentName(txtFldEqipName.getText().trim());
    }

    if (!txtFldStudentI.getText().isEmpty()) {
        selected.setBorrowerStudentId(Integer.parseInt(txtFldStudentI.getText().trim()));
    }

    if (!txtFldStudentName.getText().isEmpty()) {
        selected.setBorrowerName(txtFldStudentName.getText().trim());
    }

    if (!txtFldPhone.getText().isEmpty()) {
        selected.setBorrowerPhone(txtFldPhone.getText().trim());
    }

    if (!txtFldAddress.getText().isEmpty()) {
        selected.setBorrowerDepartment(txtFldAddress.getText().trim());
    }

    if (!txtFldCustID1.getText().isEmpty()) {
        selected.setDueDate(LocalDate.parse(txtFldCustID1.getText().trim()));
    }

    DefaultTableModel model = (DefaultTableModel) tblProduct.getModel();
    model.setValueAt(selected.getEquipmentId(), selectedRowIndex, 0);
    model.setValueAt(selected.getEquipmentName(), selectedRowIndex, 1);
    model.setValueAt(selected.getBorrowerStudentId(), selectedRowIndex, 2);
    model.setValueAt(selected.getBorrowerName(), selectedRowIndex, 3);
    model.setValueAt(selected.getDueDate() == null ? "" : selected.getDueDate().toString(), selectedRowIndex, 4);
    model.setValueAt(selected.getBorrowerPhone(), selectedRowIndex, 5);
    model.setValueAt(selected.getBorrowerDepartment(), selectedRowIndex, 6);

    lblErrorMsgEquipID.setText("");
    lblErrorMsgEquipmentName.setText("");
    lblErrorMsgStudentID.setText("");
    lblErrorMsgStudentName.setText("");
    lblErrorMsgPhoneNo.setText("");
    lblErrorMsgAddress.setText("");
    lblErrorMsgDueDate.setText("");

    JOptionPane.showMessageDialog(this, "Record updated successfully.", "Update Success", JOptionPane.INFORMATION_MESSAGE);
    clearStudentForm();

    }//GEN-LAST:event_btnUpdateActionPerformed

