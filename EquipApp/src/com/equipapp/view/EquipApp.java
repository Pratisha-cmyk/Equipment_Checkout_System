    /**
     * Event handler for the Add button action. Validates input fields, creates
     * a new student record if valid, and checks for duplicate LMU IDs before
     * adding the record to the student list.
     *
     * @param evt the ActionEvent triggered by the Add button
     */
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
     boolean isValid = true;

    isValid &= validateField(txtFldEquipID, "Equipment ID", lblErrorMsgEquipID, "Must be a positive number.",
            errorColor, successColor, ValidationUtil.isValidEquipmentId(txtFldEquipID.getText()));

    isValid &= validateField(txtFldEqipName, "Equipment Name", lblErrorMsgEquipmentName, "Only letters and spaces allowed.",
            errorColor, successColor, ValidationUtil.isValidEquipmentName(txtFldEqipName.getText()));

    isValid &= validateField(txtFldStudentI, "Student ID", lblErrorMsgStudentID, "Must be a positive number.",
            errorColor, successColor, ValidationUtil.isValidBorrowerStudentId(txtFldStudentI.getText()));

    isValid &= validateField(txtFldStudentName, "Student Name", lblErrorMsgStudentName, "Only letters and spaces allowed.",
            errorColor, successColor, ValidationUtil.isValidBorrowerName(txtFldStudentName.getText()));

    isValid &= validateField(txtFldPhone, "Phone", lblErrorMsgPhoneNo, "Phone must be 7 to 15 digits (optional +).",
            errorColor, successColor, ValidationUtil.isValidBorrowerPhone(txtFldPhone.getText()));

    isValid &= validateField(txtFldAddress, "Department", lblErrorMsgAddress, "Only letters and spaces allowed.",
            errorColor, successColor, ValidationUtil.isValidBorrowerDepartment(txtFldAddress.getText()));

    isValid &= validateField(txtFldCustID1, "Due Date", lblErrorMsgDueDate, "Use format YYYY-MM-DD.",
            errorColor, successColor, ValidationUtil.isValidDate(txtFldCustID1.getText()));

    if (!isValid) return;

    LocalDate dueDate = LocalDate.parse(txtFldCustID1.getText().trim());

    EquipmentModel newEquipment = new EquipmentModel(
            Integer.parseInt(txtFldEquipID.getText().trim()),
            txtFldEqipName.getText().trim(),
            "",
            "",
            true,
            Integer.parseInt(txtFldStudentI.getText().trim()),
            txtFldStudentName.getText().trim(),
            txtFldPhone.getText().trim(),
            txtFldAddress.getText().trim(),
            LocalDate.now(),
            dueDate,
            null
    );

    if (checkDuplicateEquipment(newEquipment)) {
        txtFldEquipID.setBorder(createTitledBorder(errorColor, "Equipment ID"));
        showDialogBox("Equipment ID already exists. Please try again!", "Duplicate Entry", JOptionPane.WARNING_MESSAGE);
        return;
    }

    registerEquipment(newEquipment);
    clearStudentForm();
    showDialogBox("Equipment record added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnAddActionPerformed

