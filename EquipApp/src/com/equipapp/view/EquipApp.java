private int sortKey() {
    // 0 = Sort by, 1 = Equipment ID, 2 = Student ID, 3 = Equipment Name
    return cmbBoxSorting.getSelectedIndex();
}

private Comparator<EquipmentModel> getComparator(int key) {
    switch (key) {
        case 1: // Equipment ID
            return Comparator.comparingInt(EquipmentModel::getEquipmentId);

        case 2: // Student ID
            return Comparator.comparingInt(EquipmentModel::getBorrowerStudentId);

        case 3: // Equipment Name
            return Comparator.comparing(
                    e -> e.getEquipmentName() == null ? "" : e.getEquipmentName().toLowerCase()
            );

        default:
            return null;
    }
}

private void sortAscending() {
    int key = sortKey();
    Comparator<EquipmentModel> comp = getComparator(key);

    if (comp == null) {
        JOptionPane.showMessageDialog(this, "Please select a sort option.", "Sort", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Collections.sort(equipmentList, comp);
    updateTable(equipmentList);
}

private void sortDescending() {
    int key = sortKey();
    Comparator<EquipmentModel> comp = getComparator(key);

    if (comp == null) {
        JOptionPane.showMessageDialog(this, "Please select a sort option.", "Sort", JOptionPane.WARNING_MESSAGE);
        return;
    }

    Collections.sort(equipmentList, comp.reversed());
    updateTable(equipmentList);
}


private void applySearch() {
    String q = jTextField1.getText();
    if (q == null) q = "";
    q = q.trim();

    if (q.isEmpty() || q.equalsIgnoreCase("search...")) {
        updateTable(equipmentList);
        return;
    }

    List<EquipmentModel> filtered = new ArrayList<>();

    for (EquipmentModel e : equipmentList) {
        String equip = e.getEquipmentName() == null ? "" : e.getEquipmentName();
        String student = e.getBorrowerName() == null ? "" : e.getBorrowerName();

        if (equip.toLowerCase().contains(q.toLowerCase()) ||
            student.toLowerCase().contains(q.toLowerCase())) {
            filtered.add(e);
        }
    }

    updateTable(filtered);
}

    
