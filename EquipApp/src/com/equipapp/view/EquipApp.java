    /**
     * Simulates the loading progress using a SwingWorker thread. Updates a
     * progress bar incrementally and switches to the login screen upon
     * completion.
     */
  private void startProgress() {
    Color mainColor = new Color(153, 0, 51);

    pgBarSplashScreen.setUI(new javax.swing.plaf.basic.BasicProgressBarUI());
    pgBarSplashScreen.setForeground(mainColor);
    pgBarSplashScreen.setBackground(Color.WHITE);
    pgBarSplashScreen.setStringPainted(false);

    pnlLoadingScreen.setBackground(Color.WHITE);
    lblLoading.setForeground(mainColor);

    javax.swing.SwingWorker<Void, Integer> worker = new javax.swing.SwingWorker<>() {
        @Override
        protected Void doInBackground() throws Exception {
            for (int i = 0; i <= 100; i++) {
                Thread.sleep(30);
                publish(i);
            }
            return null;
        }

        @Override
        protected void process(List<Integer> chunks) {
            pgBarSplashScreen.setValue(chunks.get(chunks.size() - 1));
        }

        @Override
        protected void done() {
            loadScreen("LoginScreen");
        }
    };

    worker.execute();
}


